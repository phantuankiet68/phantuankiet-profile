function openDesign(item) {
  modalA.querySelector(".dmodal-title").textContent = item.name || "";
  modalA.querySelector(".dmodal-desc").textContent = item.desc || "";
  modalA.querySelector(".dmodal-loc").textContent = item.location || "";
  modalA.querySelector(".dmodal-rateval").textContent = item.rating ?? "";

  const img = modalA.querySelector(".dmodal-img");
  img.src = item.modalImg || item.img || "";
  img.alt = item.name || "Design";

  const badge = modalA.querySelector(".dmodal-badge");
  if (item.badge) {
    badge.textContent = item.badge;
    badge.hidden = false;
  } else badge.hidden = true;

  const view = modalA.querySelector(".dmodal-actions a.primary");
  if (item.link && item.link !== "#") {
    view.href = item.link;
    view.style.display = "inline-flex";
  } else {
    view.style.display = "none";
  }

  modalA.showModal();
}

document.addEventListener("click", (e) => {
  const likeBtn = e.target.closest(".like-btn");
  if (likeBtn) {
    const card = likeBtn.closest(".place-card");
    const id = Number(card.dataset.id);
    const item = DESIGN_LIST.find((x) => x.id === id);
    if (!item) return;
    item.liked = !item.liked;
    card.setAttribute("data-liked", item.liked);
    return;
  }

  const media = e.target.closest(".place-media");
  if (!media) return;

  const card = media.closest(".place-card");
  const id = Number(card.dataset.id);
  const item = DESIGN_LIST.find((x) => x.id === id);
  if (!item) return;

  openDesign(item);
});

modalA.querySelector(".dmodal-close").addEventListener("click", () => modalA.close());
modalA.querySelector("[data-close]").addEventListener("click", () => modalA.close());

modalA.addEventListener("click", (e) => {
  const rect = modalA.getBoundingClientRect();
  const inside = e.clientX >= rect.left && e.clientX <= rect.right && e.clientY >= rect.top && e.clientY <= rect.bottom;
  if (!inside) modalA.close();
});

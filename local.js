function getPercent(valEl) {
  const raw = valEl.dataset.value ?? valEl.textContent;
  const value = Number(String(raw).replace("%", "").trim());
  return Math.max(0, Math.min(100, isNaN(value) ? 0 : value));
}

function applyOne(skill) {
  const valEl = skill.querySelector(".skill-val");
  const bar = skill.querySelector(".skill-bar");
  if (!valEl || !bar) return false;

  const cs = getComputedStyle(bar);
  if (cs.position === "static") bar.style.position = "relative";

  const p = getPercent(valEl);

  const fill = skill.querySelector(".skill-fill");
  if (fill) fill.style.width = p + "%";

  const w = bar.getBoundingClientRect().width;
  if (!w) return false;

  valEl.style.position = "absolute";
  valEl.style.left = p + "%";
  valEl.style.transform = "translateX(-50%)";

  return true;
}

function applyAll(scope = document) {
  scope.querySelectorAll(".skill").forEach((s) => applyOne(s));
}

function applyAllWhenReady(scope = document, tries = 60) {
  const skills = [...scope.querySelectorAll(".skill")];
  let okCount = 0;

  skills.forEach((s) => {
    if (applyOne(s)) okCount++;
  });

  if (okCount === skills.length) return;

  if (tries > 0) {
    requestAnimationFrame(() => applyAllWhenReady(scope, tries - 1));
  }
}

